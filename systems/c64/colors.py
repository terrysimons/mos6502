"""VIC-II color constants for the Commodore 64.

The VIC-II chip supports 16 colors, indexed 0-15.
These are the standard color codes used throughout the C64 system.
"""

# VIC-II color codes (4-bit values 0-15)
COLOR_BLACK = 0x00
COLOR_WHITE = 0x01
COLOR_RED = 0x02
COLOR_CYAN = 0x03
COLOR_PURPLE = 0x04
COLOR_GREEN = 0x05
COLOR_BLUE = 0x06
COLOR_YELLOW = 0x07
COLOR_ORANGE = 0x08
COLOR_BROWN = 0x09
COLOR_LIGHT_RED = 0x0A
COLOR_DARK_GRAY = 0x0B
COLOR_MEDIUM_GRAY = 0x0C
COLOR_LIGHT_GREEN = 0x0D
COLOR_LIGHT_BLUE = 0x0E
COLOR_LIGHT_GRAY = 0x0F
